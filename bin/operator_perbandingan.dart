void main() {
  var first = 10;
  var second = first;
  var text = 'yuda';
  // perbandingan tipe data number
  print(first == second); // true
  print(first != second); // false
  print(first > second); // false
  print(first < second); // false
  print(first >= second); // true
  print(first <= second); // true

  //perbandingan tipe data string
  print(text == 'yuda'); // true
  print(text != 'yuda'); // false
}
